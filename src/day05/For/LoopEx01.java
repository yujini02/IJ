package day05.For;

public class LoopEx01 {
    public static void main(String[] args) {

        for(int i = 1;i<=10;i++){
            System.out.print(i + " ");
        }
        System.out.println();
        int start = 0;
        while(start <=4){
            System.out.print(start+ " ");
            start++;
        }

    }
}
